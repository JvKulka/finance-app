function getByPath(obj: Dict, path: string): string | undefined {
  return path.split(".").reduce<string | Dict | undefined>((acc, part) => {
    if (!acc || typeof acc === "string") return acc;
    return acc[part];
  }, obj) as string | undefined;
}

export function useI18n() {
  const { language } = useSystemPreferences();
  const locale = language.startsWith("pt") ? "pt" : "es";
  const bundle = messages[locale];
  const fallback = messages.es;

  const t = (key: string) => getByPath(bundle, key) || getByPath(fallback, key) || key;

  return { t, language, locale };
}
